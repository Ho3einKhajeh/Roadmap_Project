let currentPath = null;
let currentQuestionIndex = 0;
let userAnswers = {};

// تابع برای بارگذاری فایل JSON
function loadJSON(path, callback) {
    const xhr = new XMLHttpRequest();
    xhr.overrideMimeType("application/json");
    xhr.open('GET', path, true);
    xhr.onreadystatechange = function () {
        if (xhr.readyState === 4 && xhr.status === 200) {
            callback(JSON.parse(xhr.responseText));
        }
    };
    xhr.send(null);
}

document.addEventListener('DOMContentLoaded', () => {
    const buttons = document.querySelectorAll('.path-btn');
    buttons.forEach(button => {
        button.addEventListener('click', () => {
            currentPath = button.dataset.path;
            currentQuestionIndex = 0;
            userAnswers = {};
            showQuestion(data[currentPath].questions);
        });
    });
});

function showQuestion(questions) {
    const question = questions[currentQuestionIndex];
    
    const container = document.querySelector('.container');
    container.innerHTML = `
        <h1>${question.question}</h1>
        <p class="question-counter">سوال ${currentQuestionIndex + 1} از ${questions.length}</p>
        <div class="buttons-container">
            ${question.options.map((option, index) => `
                <button class="answer-btn" data-answer="${index}">${option}</button>
            `).join('')}
        </div>
    `;

    const answerButtons = document.querySelectorAll('.answer-btn');
    answerButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            // همه دکمه ها از حالت انتخاب خارج شوند
            answerButtons.forEach(b => b.classList.remove('selected'));
            // دکمه کلیک شده انتخاب شود
            btn.classList.add('selected');
    
            // با یک تاخیر کوتاه (برای نمایش افکت انتخاب) سوال بعدی لود شود
            setTimeout(() => {
                userAnswers[currentQuestionIndex] = btn.dataset.answer;
                currentQuestionIndex++;
                
                if (currentQuestionIndex < questions.length) {
                    showQuestion(questions);
                } else {
                    showRoadmap(data[currentPath].roadmap);
                }
            }, 250); // 250 میلی‌ثانیه مکث
        });
    });
    
}


function showRoadmap(roadmap) {
    const container = document.querySelector('.container');
    container.innerHTML = `
        <h1>راهنمای مسیر یادگیری ${currentPath}</h1>
        <div class="roadmap">
            ${Object.entries(roadmap).map(([section, items]) => `
                <div class="roadmap-section">
                    <h2>${section}</h2>
                    ${Array.isArray(items) ? `
                        <ul>
                            ${items.map(item => `<li>${item}</li>`).join('')}
                        </ul>
                    ` : Object.entries(items).map(([subsection, subitems]) => `
                        <h3>${subsection}</h3>
                        <ul>
                            ${Array.isArray(subitems) ? `
                                ${subitems.map(item => `<li>${item}</li>`).join('')}
                            ` : Object.entries(subitems).map(([subsubsection, subsubitems]) => `
                                <h4>${subsubsection}</h4>
                                <ul>
                                    ${subsubitems.map(item => `<li>${item}</li>`).join('')}
                                </ul>
                            `).join('')}
                        </ul>
                    `).join('')}
                </div>
            `).join('')}
        </div>
        <div class="buttons-container">
            <button class="path-btn" onclick="location.reload()">شروع مجدد</button>
        </div>
    `;
} 
